'''
파일명: Ex03-1-escape.py
내용: 이스케이프 문자 학습

이스케이프 문자란?
   - 특수한 기능을 가진 문자
   - 백슬래시(\) 뒤에 특정 문자를 붙여 사용
'''

# 1. 따옴표 출력하기
print('## 따옴표 출력 ##')
print('Hello \'World\'')   # 작은따옴표 출력
print("Hello \"World\"")   # 큰따옴표 출력

# 2. 줄바꿈과 들여쓰기
print('\n## 줄바꿈(\\n) 예제 ##')
print('*\n**\n***')       # \n으로 줄바꿈
'''
출력결과:
*
**
***
'''

# 3. 탭으로 간격 맞추기
print('\n## 탭(\\t) 예제 ##')
print('이름\t연락처')        # 제목 행
print('제시카\t02-124-5678') # 데이터 행
'''
출력결과:
이름    연락처
제시카  02-124-5678
'''

# 4. 자주 사용하는 이스케이프 문자 정리
print('\n## 이스케이프 문자 정리 ##')
print('줄바꿈\n예제')       # \n: 줄바꿈
print('탭\t예제')          # \t: 탭
print('백슬래시 \\ 예제')   # \\: 백슬래시 출력